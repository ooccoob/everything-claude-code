def convert_tc_to_sc(text):
    """补充繁体转简体"""
    for tc, sc in ADDITIONAL_TC_TO_SC.items():
        text = text.replace(tc, sc)
    return text

def process_file(filepath):
    """处理单个文件"""
    try:
        with codecs.open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()

        converted_content = convert_tc_to_sc(content)

        with codecs.open(filepath, 'w', encoding='utf-8') as f:
            f.write(converted_content)

        return True, None
    except Exception as e:
        return False, str(e)

def main():
    docs_zh_cn = r'D:\mycode\everything-claude-code\docs\zh-CN'

    md_files = []
    for root, dirs, files in os.walk(docs_zh_cn):
        for file in files:
            if file.endswith('.md'):
                md_files.append(os.path.join(root, file))

    print(f'找到 {len(md_files)} 个 Markdown 文件')

    success_count = 0
    fail_count = 0

    for i, filepath in enumerate(md_files, 1):
        success, error = process_file(filepath)
        if success:
            success_count += 1
            print(f'[{i}/{len(md_files)}] ✓ {os.path.basename(filepath)}')
        else:
            fail_count += 1
            print(f'[{i}/{len(md_files)}] ✗ {os.path.basename(filepath)}: {error}')

    print(f'\n第二轮转换完成！')
    print(f'成功: {success_count} 个文件')
    print(f'失败: {fail_count} 个文件')

if __name__ == '__main__':
    main()
